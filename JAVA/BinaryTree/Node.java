package binarytree;

/**
 *
 * @author Alan
 */
public class Node {
    
    public int value;
    Node right;
    Node left;
    Node parent;

    public Node(int v){
        value=v;
    }
    
    public Node getRight() {
        return right;
    }

    public void setRight(Node right) {
        this.right = right;
    }

    public Node getLeft() {
        return left;
    }

    public void setLeft(Node left) {
        this.left = left;
    }

    public Node getParent() {
        return parent;
    }

    public void setParent(Node parent) {
        this.parent = parent;
    }
    
}
